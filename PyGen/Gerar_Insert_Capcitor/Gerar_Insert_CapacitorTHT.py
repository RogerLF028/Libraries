# ==================== FUNÇÕES AUXILIARES ====================
def sql_str(val):
    if val is None:
        return 'NULL'
    return "'" + str(val).replace("'", "''") + "'"

def format_capacitance(c):
    if c == int(c):
        return f"{int(c)}u"
    else:
        return f"{c}u"

def format_voltage_display(v):
    """Formata tensão para exibição: 6.3V → '6V3', 10V → '10V', etc."""
    if v == int(v):
        return f"{int(v)}V"
    else:
        # Converte para string e troca o ponto por 'V'
        return str(v).replace('.', 'V')

